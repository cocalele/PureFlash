inline PfMessageStatus operator | (PfMessageStatus lhs, PfMessageStatus rhs)
{
	typedef std::underlying_type <PfMessageStatus>::type T;
	return static_cast<PfMessageStatus>(static_cast<T >(lhs) | static_cast<T >(rhs));
}

